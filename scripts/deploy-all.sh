# Instructor helper: publishes the "interact" challenges (3, 4) and the Challenge 5
# Stage B vulnerable account, then prints (and saves) the ids students need.
#
# Challenge 5 Stage A is published by students themselves, so it is NOT deployed here.
#
# Prereqs: a funded active address on devnet with Account Abstraction enabled, plus
# `jq`. Override the CLI with IOTA_BIN if needed.
set -euo pipefail

IOTA="${IOTA_BIN:-iota}"
KIT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
ENV_FILE="$KIT_ROOT/.workshop.env"
GAS_BUDGET="${GAS_BUDGET:-300000000}"

ORIGINAL_ADDR="$($IOTA client active-address)"
echo "Using CLI: $($IOTA --version)"
echo "Active address: $ORIGINAL_ADDR"
echo "Active env:     $($IOTA client active-env)"
echo

# Extract the first complete JSON object from CLI output, which may be preceded by
# progress / "[note]" lines. Mirrors the official IOTA example READMEs.
extract_json() {
  awk '/{/ { if (!in_json) { in_json=1; brace_count=1 } else { brace_count++ } } /}/ { brace_count-- } in_json { print } brace_count == 0 && in_json { exit }'
}

# publish <path> [extra publish flags...] -> echoes the published package id; also
# exports PKG and METADATA_ID for the caller.
publish() {
  local path="$1"; shift
  local json
  json="$($IOTA client publish "$path" --gas-budget "$GAS_BUDGET" --json "$@" 2>/dev/null | extract_json)"
  PKG="$(echo "$json" | jq -r '.objectChanges[] | select(.type=="published") | .packageId')"
  METADATA_ID="$(echo "$json" | jq -r '.objectChanges[] | select(.objectType=="0x2::package_metadata::PackageMetadataV1") | .objectId')"
  LAST_JSON="$json"
}

echo "==> Challenge 3 (mint_master)"
publish "$KIT_ROOT/challenges/03-mint-master"
PKG3="$PKG"
TREASURY3="$(echo "$LAST_JSON" | jq -r '.objectChanges[] | select((.objectType // "") | test("::coin::TreasuryCap<.*::mint_master::MINT_MASTER>")) | .objectId')"
echo "    PKG3=$PKG3"
echo "    TREASURY3=$TREASURY3"
echo

echo "==> Challenge 4 (ingredient_heist)"
publish "$KIT_ROOT/challenges/04-ingredient-heist"
PKG4="$PKG"
echo "    PKG4=$PKG4"
echo

echo "==> Challenge 5 Stage B (vulnerable_account)"
publish "$KIT_ROOT/challenges/05-account-abstraction/vulnerable-account" --with-unpublished-dependencies
VPKG="$PKG"
VMETA="$METADATA_ID"
echo "    VPKG=$VPKG"
# Create the vulnerable account, owned by an all-zero public key nobody controls.
ZERO_PK="vector[$(printf '0,%.0s' $(seq 1 31))0]"   # 32 zero bytes
PTB_JSON="$($IOTA client ptb \
  --move-call 0x2::authenticator_function::create_auth_function_ref_v1 "<$VPKG::vulnerable_account::VulnAccount>" @"$VMETA" "\"vulnerable_account\"" "\"authenticate\"" \
  --assign ref \
  --move-call "$VPKG::vulnerable_account::create" "$ZERO_PK" ref \
  --gas-budget "$GAS_BUDGET" --json 2>/dev/null | extract_json)"
VACC="$(echo "$PTB_JSON" | jq -r '.objectChanges[] | select((.objectType // "") | endswith("::vulnerable_account::VulnAccount")) | .objectId')"
echo "    VACC=$VACC"
echo

# Creating an abstract account via the CLI switches the active address to it.
# Restore the operator's original address so later commands run as them.
if [ "$($IOTA client active-address)" != "$ORIGINAL_ADDR" ]; then
  $IOTA client switch --address "$ORIGINAL_ADDR" >/dev/null
  echo "Restored active address to $ORIGINAL_ADDR"
  echo
fi

cat >"$ENV_FILE" <<EOF
# Generated by deploy-all.sh — source this, then hand the ids to students.
export PKG3=$PKG3
export TREASURY3=$TREASURY3
export PKG4=$PKG4
export VPKG=$VPKG
export VACC=$VACC
EOF

echo "Saved ids to $ENV_FILE"
echo
echo "  Challenge 3:  package=$PKG3  treasury=$TREASURY3"
echo "  Challenge 4:  package=$PKG4"
echo "  Challenge 5B: package=$VPKG  account=$VACC"
